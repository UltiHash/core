#ifndef IO_DATA_GENERATOR_H
#define IO_DATA_GENERATOR_H

#include <optional>
#include <span>
#include <variant>
#include <vector>


namespace uh::io
{

// ---------------------------------------------------------------------

/**
 * Piece of data to be returned by `data_generator`. Can hold a vector
 * or a reference to some external buffer and makes it accessible through
 * a single function.
 *
 * class invariant: m_buffer.empty() != m_span.empty()
 */
class data_chunk
{
public:
    data_chunk(std::vector<char>&& buffer);
    data_chunk(std::span<const char> span);

    std::span<const char> data();
private:
    std::variant<std::vector<char>, std::span<const char>> m_data;
};

// ---------------------------------------------------------------------

class data_generator
{
public:
    virtual ~data_generator() = default;

    /**
     * Get the next sub-chunk. The function will return subchunk until
     * all subchunks are provided. If there are no more subchunks to
     * return `std::nullopt` will be returned.
     */
    virtual std::optional<data_chunk> next() = 0;

    /**
     * If possible, return the amount of data that will be generated by
     * this class in bytes.
     *
     * @throws util::exception if the size cannot be provided
     */
    virtual std::size_t size() const = 0;
};

// ---------------------------------------------------------------------

} // namespace uh::io

#endif
